function SongEntry(name, index) {
  this.name = name.trim();
  this.value = this.name;
  this.text = this.name;
  this.key = index;

  // tbd
  this.filename = "";
  this.author = "";
  this.songkey = "";
  this.styles = "";
}

function SongList(name, key, type = "default") {
  this.name = name;
  this.value = name;
  this.text = name;
  this.type = type;
  this.key = key;
  this.list = [];
}

